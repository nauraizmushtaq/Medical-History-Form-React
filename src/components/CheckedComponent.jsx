import React from "react";
/*
Simple Componeent for manipulation of the awesome icon
*/
/*
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
    */
const CheckedComponent = (props) => {
  let classes = "fa fa-check-circle fa-lg";
  var colord = "#00a6d4";
  if (!props.reg) {
    classes = "fa fa-circle-o fa-lg";
    colord = "#302b2bb6";
  }

  /*
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
  --------------------------------------------------------------------------------------------------------------------------
    */
  return (
    <i
      onClick={props.onClick}
      style={{ cursor: "pointer", color: colord }}
      className={classes}
      aria-hidden="true"
    />
  );
};

export default CheckedComponent;
